export async function getReservationsByUserId({
  userId,
}: {
  userId: string;
}) {
  try {
    const rows = await db
      .select()
      .from(reservation)
      .where(eq(reservation.userId, userId))
      .orderBy(desc(reservation.createdAt));

    // Ensure details is an object
    return rows.map((r: any) => ({
      ...r,
      details: typeof r.details === "string" ? safeJsonParse(r.details) : r.details,
    }));
  } catch (error) {
    console.error("Failed to get reservations by user from database");
    throw error;
  }
}

// --- helpers ---
function safeJsonParse(str: string) {
  try {
    return JSON.parse(str);
  } catch {
    return str;
  }
}
